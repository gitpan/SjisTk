=head1 ソフトウェア名称

SjisTk-JA - Tk modules on Microsoft Windows with Sjis software (Japanese document)

=head1 概要

   use Sjis 0.64;
   use Tk;

=head1 要約

このソフトウェアは Microsoft Windows と Sjis ソフトウェアの組合せて使用
する Tk モジュールです。

JPerl で記述した Perl/Tk アプリケーションを Perl5.8 またはそれ以降に移植
するときに役立ちます。

